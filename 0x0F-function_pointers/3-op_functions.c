#include "3-calc.h"

/**
 * op_add - add two intgers
 * @a: the first int
 * @b: the second int
 * Return: the reslut
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - difference between  two intgers
 * @a: the first int
 * @b: the second int
 * Return: the reslut
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - the product of two integers
 * @a: the first int
 * @b: the second int
 * Return: the result
*/

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - the divison of two intgers
 * @a: the first int
 * @b: the second int
 * Return: the result
*/

int op_div(int a, int b)
{
return (a / b);
}


/**
 * op_mod - remainder of the division of
 * @a: the first int
 * @b: the second int
 * Return: the result
*/

int op_mod(int a, int b)
{
return (a % b);
}
